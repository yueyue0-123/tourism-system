def make_paragraph(doc, text, size=14, bold=None, align=WD_ALIGN_PARAGRAPH.JUSTIFY,
                   font_name='Times New Roman', space_before=None, space_after=None):
    """Создаёт параграф, копируя формат из существующего."""
    p = doc.add_paragraph()
    p.alignment = align
    if space_before is not None:
        p.paragraph_format.space_before = Pt(space_before)
    if space_after is not None:
        p.paragraph_format.space_after = Pt(space_after)
    run = p.add_run(text)
    run.font.name = font_name
    run.font.size = Pt(size)
    if bold is not None:
        run.bold = bold
    # Настройка eastAsia
    from docx.oxml.ns import qn
    from docx.oxml import OxmlElement
    rPr = run._element.get_or_add_rPr()
    rFonts = rPr.find(qn('w:rFonts'))
    if rFonts is None:
        rFonts = OxmlElement('w:rFonts')
        rPr.insert(0, rFonts)
    rFonts.set(qn('w:eastAsia'), font_name)
    return p

def insert_before(ref_para, new_para):
    """Вставляет новый параграф перед ref_para."""
    ref_para._element.addprevious(new_para._element)

